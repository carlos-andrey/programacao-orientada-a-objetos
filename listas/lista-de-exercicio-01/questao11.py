numero1 = int(input('Informe o primeiro numero: '))
numero2 = int(input('Informe o segundo numero: '))
numero3 = float(input('informe o terceiro numero: '))
dobro_metade = (numero1 * 2) * (numero2 / 2)
triplo_terceiro = (numero1 * 3) + numero3
terceiro_cubo = numero3 ** 3
print('O produto do dobro do primeiro numero com a metade do segundo numero é {}'.format(dobro_metade))
print('A soma do triplo do primeiro numero com o terceiro numero é {}'.format(triplo_terceiro))
print('O terceiro numero elevado ao cubo é {}'.format(terceiro_cubo))